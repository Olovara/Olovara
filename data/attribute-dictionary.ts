// Helper function to get all attribute types
export const getAttributeTypes = (): (keyof AttributeDictionary)[] => {
  return Object.keys(ATTRIBUTE_DICTIONARY) as (keyof AttributeDictionary)[];
};

// Helper function to get values for a specific attribute type
export const getAttributeValues = (
  type: keyof AttributeDictionary
): string[] => {
  return ATTRIBUTE_DICTIONARY[type] || [];
};

// Helper function to normalize an attribute value (lowercase, trim)
export const normalizeAttributeValue = (value: string): string => {
  return value.toLowerCase().trim();
};

// Helper function to check if a value exists in the dictionary for a given type
export const isValidAttributeValue = (
  type: keyof AttributeDictionary,
  value: string
): boolean => {
  const normalized = normalizeAttributeValue(value);
  return ATTRIBUTE_DICTIONARY[type]?.includes(normalized) || false;
};
